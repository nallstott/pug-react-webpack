function alternativeDecks(deck, deckType) {
	let holdingDeck = [...deck];
	let holdingDoubleDeck = holdingDeck.concat([...deck]);
	let newDeck = [];
	let activeDeck;
	if (deckType == 0.5 || deckType == 1) {
		activeDeck = [...holdingDeck];
	} else if (deckType == 1.5 || deckType == 2) {
		activeDeck = [...holdingDoubleDeck];
	} else {
		console.log("You have entered an invalid deckType parameter");
	}
	while (newDeck.length < deck.length * deckType) {
		let randInt = Math.floor(Math.random() * activeDeck.length);
		newDeck.push(activeDeck[randInt]);
		activeDeck.splice(randInt, 1);
	}
	return newDeck;
}

